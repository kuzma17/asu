        //return $this->render('page', ['page' => $page]);
    }

    public function actionCoordinate()
    {
        return $this->render('coordinates');
    }


    public function actionFind(){

        $model = new Search();
        if($model->load(Yii::$app->request->post()) && $model->validate()){
            $list = Page::find()->filterWhere(['like', 'text', $model['search']])->all();
            if(count($list) == 0){
                $errors = 'По Вашему запросу ничего не найдено. Попробуйте задать другой запрос.';
                return $this->render('search', ['search'=>$model['search'], 'errors' => $errors]);
            }else {
                return $this->render('search', ['search' => $model['search'], 'list' => $list]);
            }
        }
    }
}
